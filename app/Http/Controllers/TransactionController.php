     */
    public function store(Request $request)
    {
        $transaction = new Transactions();

        $transaction->user_id = $request->user_id;
        $transaction->total_price = $request->total_price;
        $transaction->total_amount = $request->total_amount;

        try {
            $transaction->save();
        } catch (\Throwable $th) {
            throw $th;
        }
    }

